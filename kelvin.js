//The variable type is const
const kelvin = 293;

//The converting og Kelvin in Celsius

var celsius = kelvin - 273;

//The converting of celsius in fahrenheit
var fahrenheit = celsius * (9 / 5) + 32;

//The round of the fahrenheit var
Math.floor(fahrenheit);

console.log('The temperature is ' + fahrenheit + ' degrees Fahrenheit');


